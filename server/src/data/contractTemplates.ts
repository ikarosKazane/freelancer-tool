/** 获取所有合同模板列表（用于模板库展示） */
export function getContractTemplateList(): Array<{
  type: ContractTemplateType;
  name: string;
  summary: string;
}> {
  return Object.values(contractTemplates).map(({ type, name, summary }) => ({
    type,
    name,
    summary,
  }));
}

/** 获取指定类型的合同模板详情 */
export function getContractTemplate(
  type: ContractTemplateType,
): ContractTemplate | undefined {
  return contractTemplates[type];
}

